type IconGlyph(partialImport: obj -> ReactElement list -> ReactElement) =
    member _.PartialImport = partialImport
   
type IconBuilder(partialImport: IconGlyph) =
    inherit ReactBuilder()
    
    member x.Run(s: DSLElement) =
        partialImport.PartialImport (createObj s.Attributes) s.Children
       
